/**
 * This class represents the VectorIndexOutOfBoundsException.
 * @author Henry Chen
 * @version 1.2
 */
public class VectorIndexOutOfBoundsException extends IndexOutOfBoundsException {
    /**
     * Creates a new VectorIndexOutOfBoundsException.
     */
    public VectorIndexOutOfBoundsException() {
        super();
    }
    /**
     * Creates a new VectorIndexOutOfBoundsException
     * @param  msg Message passed to exception
     */
    public VectorIndexOutOfBoundsException(String msg) {
        super(msg);
    }
}