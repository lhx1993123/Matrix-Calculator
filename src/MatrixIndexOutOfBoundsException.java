/**
 * This class represents the MatrixIndexOutOfBoundsException.
 * @author Henry Chen
 * @version 1.2
 */
public class MatrixIndexOutOfBoundsException extends IndexOutOfBoundsException {
    /**
     * Creates a new MatrixIndexOutOfBoundsException
     */
    public MatrixIndexOutOfBoundsException() {
        super();
    }
    /**
     * Creates a new MatrixIndexOutOfBoundsException
     * @param  msg Message passed to exception.
     */
    public MatrixIndexOutOfBoundsException(String msg) {
        super(msg);
    }
}